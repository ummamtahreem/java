public class operators {
    public static void main(String[] args) {
        int a = 4;
        int b = 2;

        System.out.println("addition = " + (a + b));
        System.out.println("subtraction = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("true && false = " + (true && false));
        System.out.println("a | b = " + (a | b));
        System.out.println("a & b = " + (a & b));
    }
}
    

